<script lang="ts">
	import type { HTMLAttributes } from 'svelte/elements';
	import Avatar from '../../ui/Avatar/Avatar.svelte';
	import { cn } from '../../utils';

	interface IMessageProps extends HTMLAttributes<HTMLButtonElement> {
		avatar: string;
		username: string;
		text: string;
		unread?: boolean;
		callback: () => void;
	}

	const {
		avatar,
		username,
		text,
		unread = false,
		callback,
		...restProps
	}: IMessageProps = $props();

	const messageText = $derived(text.length < 80 ? text : `${text.substring(0, 80)}...`);
</script>

<button
	{...restProps}
	class={cn([
		'relative flex w-full cursor-pointer items-center gap-2 rounded-lg py-4',
		restProps.class
	])}
	onclick={callback}
>
	<Avatar src={avatar} alt="User Avatar" size="md" />
	<span class="flex w-full flex-col items-start justify-end gap-2">
		<span class="flex w-full items-center justify-between">
			<h2 class="text-white">{username}</h2>
			{#if unread}
				<span class="h-2 w-2 rounded-full bg-blue-500"></span>
			{/if}
		</span>
		<p class="text-black-200 text-start">{messageText}</p>
	</span>
</button>

